#define   _CRT_SECURE_NO_WARNINGS  1

#include<stdio.h>
int main()
{
	int a = 0;
	int b = 0;
	int c = 0;
	scanf("%d%d%d", &a, &b, &c);//%d中间有什么，输入程序就用什么继续程序，此处为空格
	if (a < b)
	{
		int tmp = a;//tmp为平时变量
		 a = b;
		 b = tmp;
	}
	if (a < c)
	{
		int tmp = a;
		 a = c;
		 c = tmp;
	}
	if (b < c)
	{
		int tmp = b;
		b = c;
		c = tmp;
	}
	printf("%d%d%d\n", a, b, c);//a放最大值，b次之，c最后
	return 0;
}